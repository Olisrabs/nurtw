

function validatePassword() {
    const password = document.getElementById("password").value;
    const confirmPassword = document.getElementById("confirmPassword").value;
    const feedback = document.getElementById("feedback");
    const feedback2 = document.getElementById("feedback2");

    if (password.length < 8) {
        feedback.innerHTML = "<span style='color: red;'>Password must be at least 8 characters long</span>";
    } else {
        feedback.innerHTML = "<span style='color: green;'>Password is valid</span>";
    }

    if (password !== confirmPassword) {
        feedback2.innerHTML = "<span style='color: red;'>Passwords do not match</span>";
    } else {
        feedback2.innerHTML = "<span style='color: green;'>Passwords match</span>";
    }
}

function showNextStep(currentStep) {

    // Validate current step before moving to the next
    if (currentStep === 1) {
        const name = document.getElementById("name").value;
        const phone = document.getElementById("phone").value;
        const dob = document.getElementById("dob").value;
        const gender = document.getElementById("gender").value;
        const stateOfOrigin = document.getElementById("state_of_origin").value;
        const lga = document.getElementById("lga").value;

        if (!name || !phone || !dob || !gender || !stateOfOrigin || !lga) {
            alert("Please fill in all fields.");
            return;
        } else {
            // Hide the current step
            document.getElementById("step" + currentStep).classList.add("hidden");
            // Show the next step
            document.getElementById("step" + (currentStep + 1)).classList.remove("hidden");
        }
    } else if (currentStep === 2) {
        const stateOfWork = document.getElementById("state_of_work").value;
        const stationOfWork = document.getElementById("station_of_work").value;
        const vehicle = document.getElementById("vehicle").value;
        const vin = document.getElementById("vin").value;
        const driverLicense = document.getElementById("driver_license").value;

        if (!stateOfWork || !stationOfWork || !vehicle || !vin || !driverLicense) {
            alert("Please fill in all fields.");
            return;
        } else {
            // Hide the current step
            document.getElementById("step" + currentStep).classList.add("hidden");
            // Show the next step
            document.getElementById("step" + (currentStep + 1)).classList.remove("hidden");
        }
    }
}

function showPreviousStep(currentStep) {
    // Hide the current step
    document.getElementById("step" + currentStep).classList.add("hidden");
    // Show the previous step
    document.getElementById("step" + (currentStep - 1)).classList.remove("hidden");
}

